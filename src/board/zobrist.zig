const mboard = @import("../board.zig");
const Board = mboard.Board;
const bitboard = @import("../board/bitboard.zig");

const NUM_SIDE = mboard.NUM_SIDE;
const NUM_PIECE_TYPE = mboard.NUM_PIECE_TYPE;

pub fn initZobristKey(board: *Board) void {
    var key: u64 = 0;
    // pieces hash
    for (0..NUM_SIDE) |i_side| {
        for (0..NUM_PIECE_TYPE) |i_piece| {
            var bb = board.piece_bb[i_side][i_piece];
            while (bb != 0) {
                const loc = bitboard.removeLS1B(&bb);
                const rand_idx: usize = i_side * PIECE_COLOR_RANDOM_OFFSET + i_piece * 64 + loc;
                key ^= ZOBRIST_RANDOMS[rand_idx];
            }
        }
    }
    // side to move hash;
    var idx: usize = @intCast(@intFromEnum(board.state.turn));
    key ^= ZOBRIST_RANDOMS[SIDE_RANDOM_OFFSET + idx];

    // castling;
    idx = @intCast(board.state.castling_rights);
    key ^= ZOBRIST_RANDOMS[CASTLING_RANDOM_OFFSET + idx];

    // enpassant square
    if (board.state.en_passant) |square| {
        idx = @intCast(@intFromEnum(square));
        key ^= ZOBRIST_RANDOMS[ENPASSANT_RANDOM_OFFSET + idx];
    }
    board.state.key = key;
}

